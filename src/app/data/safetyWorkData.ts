export interface SafetyWorkKpi {
  total: number;
  byStatus: Record<WorkStatus, number>;
  byCategory: Record<WorkCategory, number>;
  byImpact: Record<ImpactLevel, number>;
  bySafetyCode: Record<SafetyCode, number>;
  nightWorkCount: number;
  completionRate: number;
}

export interface TeamWorkSummary {
  team: WorkTeam;
  hq: HqDivision;
  total: number;
  completed: number;
  inProgress: number;
  scheduled: number;
  delayed: number;
  highRisk: number;
}

