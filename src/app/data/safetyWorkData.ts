export function getSafetyKpi(hq?: HqDivision, team?: WorkTeam): SafetyWorkKpi {
  const rows = safetyWorks.filter((w) => (!hq || w.hq === hq) && (!team || w.team === team));
  const byStatus: Record<WorkStatus, number> = { 예정: 0, 진행중: 0, 완료: 0, 지연: 0, 취소: 0 };
  const byCategory: Record<WorkCategory, number> = {
    지장이설: 0,
    구축: 0,
    유지보수: 0,
    장애복구: 0,
    파라미터변경: 0,
    B2B: 0,
  };
  const byImpact: Record<ImpactLevel, number> = { 1: 0, 2: 0, 3: 0, 4: 0 };
  const bySafetyCode: Record<SafetyCode, number> = { C1: 0, C2: 0, C3: 0, C4: 0 };
  rows.forEach((w) => {
    byStatus[w.status]++;
    byCategory[w.category]++;
    byImpact[w.impactLevel]++;
    bySafetyCode[w.safetyCode]++;
  });
  const total = rows.length;
  return {
    total,
    byStatus,
    byCategory,
    byImpact,
    bySafetyCode,
    nightWorkCount: rows.filter((w) => w.isNightWork).length,
    completionRate: total > 0 ? Math.round((byStatus["완료"] / total) * 100) : 0,
  };
}

export function getTeamSummaries(hq?: HqDivision): TeamWorkSummary[] {
  const hqMap: Record<WorkTeam, HqDivision> = {
    "중부안전/구축팀": "central",
    "서부안전/구축팀": "west",
    "중부유선/설비팀": "central",
    "서부유선/설비팀": "west",
    충남Access운용팀: "central",
    충북Access운용팀: "central",
    전남Access운용팀: "west",
    전북Access운용팀: "west",
    제주Access운용팀: "west",
  };
  return (Object.keys(hqMap) as WorkTeam[])
    .filter((t) => !hq || hqMap[t] === hq)
    .map((team) => {
      const rows = safetyWorks.filter((w) => w.team === team);
      return {
        team,
        hq: hqMap[team],
        total: rows.length,
        completed: rows.filter((w) => w.status === "완료").length,
        inProgress: rows.filter((w) => w.status === "진행중").length,
        scheduled: rows.filter((w) => w.status === "예정").length,
        delayed: rows.filter((w) => w.status === "지연").length,
        highRisk: rows.filter((w) => w.safetyCode === "C3" || w.safetyCode === "C4").length,
      };
    });
}

